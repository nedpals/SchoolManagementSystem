    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            Professor selectedProf = this.professors[this.professorSelector.getSelectedIndex()];
            
            if (this.sub == null) {
                this.sub = new Subject(
                        this.nameField.getText(),
                        Integer.valueOf(this.unitsField.getText()),
                        selectedProf.id,
                        this.descriptionField.getText()
                );
                
                selectedProf.createSubject(this.sub);
            } else {
                this.sub.name = this.nameField.getText();
                this.sub.description = this.descriptionField.getText();
                this.sub.units = Integer.valueOf(this.unitsField.getText());
                this.sub.professorId = selectedProf.id;
                this.sub.save();
            }
            
            JOptionPane.showMessageDialog(rootPane, "Subject saved successfully.");
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_saveButtonActionPerformed

