    void loadDefaults() {
        initComponents();
        loadProfessors();
        
        if (sub != null) {
            this.nameField.setText(sub.name);
            this.descriptionField.setText(sub.description);
            this.unitsField.setText(String.valueOf(sub.units));
        }
    }
    
    void loadProfessors() {
        try {
            this.professors = Professor.getAll();
            this.professorSelector.removeAllItems();
            
            for (int i = 0; i < this.professors.length; i++) {
                this.professorSelector.addItem(this.professors[i].name);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
            this.professorSelector.setEnabled(false);
            this.dispose();
            //this.professors = new Professor[0];
        }
    }

